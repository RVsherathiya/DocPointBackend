export const translate = (text: string, lang: string = 'en'): string => {
  const locale = translations[lang] ? lang : 'en';
  const dict = translations[locale];

  if (dict[text]) {
    return dict[text];
  }

  if (text.startsWith('Validation failed:')) {
    const originalErrors = text.replace('Validation failed:', '').trim();
    const errors = originalErrors.split(',').map(err => {
      const parts = err.split(':');
      if (parts.length >= 2) {
        const fieldPath = parts[0].trim();
        const msg = parts.slice(1).join(':').trim();
        const translatedMsg = dict[msg] || msg;
        return `${fieldPath}: ${translatedMsg}`;
      }
      return err.trim();
    });
    return (dict['validation_failed'] || 'Validation failed: ') + errors.join(', ');
  }

  let translatedText = text;
  Object.keys(translations.en).forEach((key) => {
    if (key.length > 3 && translatedText.includes(key)) {
      translatedText = translatedText.replace(key, dict[key]);
    }
  });

  return translatedText;
};
